# CALCULO DEL VOLUMEN DEL CILINDRO RECTO
# INPUT
cuerpo_figura=input("ingrese el cuerpo geometrico:")
area_base=float(input("ingrese el area de la base del cilindro:"))
haltura=float(input("ingrese la haltura del cilindro:"))

# PROCESSING
volumen= (area_base*haltura)

# OUTPUT
print("##########################################")
print("    CUERPO GEOMETRICO:", cuerpo_figura)
print("##########################################")
print("AREA DE LA BASE=", area_base)
print("HALTURA=", haltura) 
print("##########################################")
print("VOLUMEN=", volumen)
