# CALCULO DE UNA FUERZA
# INPUT
masa=float(input("ingrese la masa en kg:"))
aceleracion=float(input("ingrese la aceleracion en m/s2:"))

# PROCESSING
fuerza= (masa*aceleracion)

# OUTPUT
print("#####################################")
print("      CALCULO DE LA FUERZA           ")
print("#####################################")
print("MASA=", masa)
print("            ACELERACION=", aceleracion)
print("#####################################")
print("FUERZA=", fuerza)

